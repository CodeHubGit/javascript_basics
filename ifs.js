/*

If statement practice.

*/

//an if statement will execute a block of code betwee the { } 
//if the boolean value or expression inside of the ( ) is true

var myBoolean = true;

if (myBoolean) {
	console.log("this code has been executed!");
}

//you can also you an else statment. and else statement will be run
//in the event that the value passed to the if statment is false

var myFalseBoolean = false;

if (myFalseBoolean) {
	console.log("this code will not run");
} else {
	console.log("this code will run though");
}